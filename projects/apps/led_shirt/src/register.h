#ifndef REGISTER_H_
#define REGISTER_H_
/*
0x00	 SET LED ON/OFF
0x01 	TOGGE LED
0x02 	SET RED   COLOR 1 LED
0x03 	SET GREEN COLOR 1 LED
0x04 	SET BLUE  COLOR 1 LED
0x02 	SET RED   COLOR 2 LED
0x03 	SET GREEN COLOR 2 LED
0x04 	SET BLUE  COLOR 2 LED
0x05 	ENABLE COLOR FADE
0x06 	SET COLOR SPEED LED
0x07 	SET STROBE SPEED LED
0x08 	SET BRIGHTNESS 1 LED
0x09 	SET BRIGHTNESS 2 LED
0x0A 	ENABLE BRIGHTNESS FADE
0x0B 	0xA0 SET BRIGHTNESS SPEED LED
*/

#define REG_LED_SET 0x00
#define REG_LED_TOGGLE 0x01

#endif /* REGISTER_H_ */
